/**
  * @brief  Conversion complete callback in non blocking mode
  * @param  AdcHandle : AdcHandle handle
  * @note   This example shows a simple way to report end of conversion, and
  *         you can add your own implementation.
  * @retval None
  */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *AdcHandle)
{
  /* Get the converted value of regular channel */
  uhADCxConvertedValue = HAL_ADC_GetValue(AdcHandle);
  duty_cycle = map(uhADCxConvertedValue, 1, 4096, 1, 100);
  if (pwm_channel == PWM_CHANNEL_1) {
	  // Change Duty Cycle for PWM_Channel_1
	  htim1.Instance->CCR1 = duty_cycle;
  }
  if (pwm_channel == PWM_CHANNEL_2) {
	  // Change Duty Cycle for PWM_Channel_2
	  htim1.Instance->CCR2 = duty_cycle;
  }
}

/**
 * @brief map() is used to map range of bvalues to another range
 */
uint16_t map(uint16_t x, uint16_t in_min, uint16_t in_max, uint16_t out_min, uint16_t out_max) {
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}

/**
  * @brief  EXTI line detection callback.
  * @param  GPIO_Pin Specifies the port pin connected to corresponding EXTI line.
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	// Switch PWM Channel
	if (pwm_channel == PWM_CHANNEL_1) {
		pwm_channel = PWM_CHANNEL_2;
	} else {
		pwm_channel = PWM_CHANNEL_1;
	}
}
